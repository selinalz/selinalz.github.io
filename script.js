
//box - Cross-Platform Storytelling
//get "box" div using getElementsByClassName, put it inside a variable
var link = document.getElementsByClassName("box")[0];
//makes the variable listen for mouseover
link.addEventListener("mouseover", Show);

//when it mouse hovers over "box", trigger mouseover function that makes image appear and changes the color of the text
function Show() {
	var img = document.getElementsByClassName("image")[0];
	img.style.opacity = "100%";
	var txt = document.getElementsByClassName("link")[0];
	txt.style.backgroundColor = "black";
	txt.style.color = "white";
	var circle = document.getElementsByClassName("circle")[0];
	circle.style.backgroundColor = "black";
}

//get "box" div using getElementsByClassName, put it inside a variable
var link = document.getElementsByClassName("box")[0];
//makes the variable listen for mouseover
link.addEventListener("mouseout", Hide);

//when mouse moves away from the "box", trigger mouseout function that returns everything to default
function Hide() {
	var img = document.getElementsByClassName("image")[0];
	img.style.opacity = "0%";
	var txt = document.getElementsByClassName("link")[0];
	txt.style.backgroundColor = "transparent";
	txt.style.color = "black";
	var circle = document.getElementsByClassName("circle")[0];
	circle.style.backgroundColor = "transparent";
}

